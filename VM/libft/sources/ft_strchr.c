#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s2;
	char	c2;
	int		i;

	s2 = (char *)s;
	c2 = c;
	i = 0;
	while (s2[i] != '\0')
	{
		if (s2[i] == c2)
			return (&s2[i]);
		i++;
	}
	if (c2 == '\0')
		return (&s2[i]);
	return (NULL);
}
