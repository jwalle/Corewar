#include <libcorewar.h>

void	ft_trim(char **s)
{
	int		i;
	char	*tmp;

	i = 0;
	while ((*s)[i] == 32 || (*s)[i] == '\n' || (*s)[i] == '\t')
		i++;
	tmp = *s;
	*s = ft_strdup(*s + i);
	free(tmp);
	i = ft_strlen(*s) - 1;
	while ((*s)[i] == 32 || (*s)[i] == '\n' || (*s)[i] == '\t')
		i--;
	(*s)[i + 1] = '\0';
	i = 0;
	while ((*s)[i] != '\0')
	{
		if ((*s)[i] == '\t')
			(*s)[i] = 32;
		i++;
	}
}
